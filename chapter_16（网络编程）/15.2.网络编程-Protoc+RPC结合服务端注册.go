type OrderService struct {
}

func (ov *OrderService) GetOrderInfo(request OrderRequest, response *OrderInfo) error {
	orderMap := map[string]OrderInfo{
		"20220326": OrderInfo{OrderId: "20220326", OrderName: "衣服", OrderStatus: "已付款"},
		"20220327": OrderInfo{OrderId: "20220327", OrderName: "裤子", OrderStatus: "已付款"},
		"20220328": OrderInfo{OrderId: "20220328", OrderName: "手套", OrderStatus: "未付款"},
	}
	current := time.Now().Unix()
	if request.TimeStamp > current {
		*response = OrderInfo{OrderId: "0", OrderName: "", OrderStatus: "订单信息异常"}
	} else {
		result := orderMap[request.OrderId]
		if result.OrderId != "" {
			*response = orderMap[request.OrderId]
		} else {
			return errors.New("服务器错误")
		}
	}
	return nil
}

func main() {
	orderService := new(OrderService)
	rpc.Register(orderService)
	rpc.HandleHTTP()
	listen, err := net.Listen("tcp", ":8081")
	if err != nil {
		panic(err.Error())
	}
	http.Serve(listen, nil)
}

